    public function filterByLinks($links, $comparison = null)
    {
        if ($links instanceof \Engine\Links) {
            return $this
                ->addUsingAlias(UsersTableMap::COL_ID, $links->getUserId(), $comparison);
        } elseif ($links instanceof ObjectCollection) {
            return $this
                ->useLinksQuery()
                ->filterByPrimaryKeys($links->getPrimaryKeys())
                ->endUse();
        } else {
            throw new PropelException('filterByLinks() only accepts arguments of type \Engine\Links or Collection');
        }
    }

    /**
     * Adds a JOIN clause to the query using the Links relation
     *
     * @param     string $relationAlias optional alias for the relation
     * @param     string $joinType Accepted values are null, 'left join', 'right join', 'inner join'
     *
     * @return $this|ChildUsersQuery The current query, for fluid interface
     */
    public function joinLinks($relationAlias = null, $joinType = Criteria::INNER_JOIN)
    {
        $tableMap = $this->getTableMap();
        $relationMap = $tableMap->getRelation('Links');

        // create a ModelJoin object for this join
        $join = new ModelJoin();
        $join->setJoinType($joinType);
        $join->setRelationMap($relationMap, $this->useAliasInSQL ? $this->getModelAlias() : null, $relationAlias);
        if ($previousJoin = $this->getPreviousJoin()) {
            $join->setPreviousJoin($previousJoin);
        }

        // add the ModelJoin to the current object
        if ($relationAlias) {
            $this->addAlias($relationAlias, $relationMap->getRightTable()->getName());
            $this->addJoinObject($join, $relationAlias);
        } else {
            $this->addJoinObject($join, 'Links');
        }

        return $this;
    }

    /**
