    public function useLinksQuery($relationAlias = null, $joinType = Criteria::INNER_JOIN)
    {
        return $this
            ->joinLinks($relationAlias, $joinType)
            ->useQuery($relationAlias ? $relationAlias : 'Links', '\Engine\LinksQuery');
    }

    /**
     * Exclude object from result
